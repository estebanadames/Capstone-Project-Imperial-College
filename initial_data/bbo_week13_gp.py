def expected_improvement(X, gp, y_best, xi=0.01):
    mu, sigma = gp.predict(X, return_std=True)
    sigma = np.maximum(sigma, 1e-12)
    imp = mu - y_best - xi
    z = imp / sigma
    return imp * norm.cdf(z) + sigma * norm.pdf(z)

def latin_hypercube(n, d, seed=42):
    sampler = qmc.LatinHypercube(d=d, seed=seed)
    return sampler.random(n)

def format_query(x):
    return "-".join(f"{v:.6f}" for v in x)

def propose_query(X, y, func_idx):
    d = X.shape[1]

    kernel = (
        ConstantKernel(1.0)
        * Matern(length_scale=np.ones(d), nu=2.5)
        + WhiteKernel(noise_level=1e-5)
    )

    gp = GaussianProcessRegressor(
        kernel=kernel,
        normalize_y=True,
        n_restarts_optimizer=12,
        random_state=42
    )
    gp.fit(X, y)

    if func_idx == 4:      # Function 5: strongest trend
        n_global = 500
        local_noise = 0.0015
        xi = 0.00005
    elif func_idx == 7:    # Function 8: micro-refinement
        n_global = 500
        local_noise = 0.0025
        xi = 0.00005
    elif func_idx == 6:    # Function 7: return around best
        n_global = 1000
        local_noise = 0.004
        xi = 0.0002
    elif func_idx == 5:    # Function 6: avoid latest poor region
        n_global = 8000
        local_noise = 0.025
        xi = 0.020
    elif func_idx == 3:    # Function 4: new best, exploit
        n_global = 1200
        local_noise = 0.004
        xi = 0.0005
    elif func_idx == 1:    # Function 2: new best, exploit carefully
        n_global = 3000
        local_noise = 0.012
        xi = 0.002
    elif func_idx == 2:    # Function 3: still unresolved
        n_global = 20000
        local_noise = 0.080
        xi = 0.060
    elif func_idx == 0:    # Function 1: stable / saturated
        n_global = 50000
        local_noise = 0.150
        xi = 0.250
    else:
        n_global = 12000
        local_noise = 0.030
        xi = 0.010

    n_total = 65000
    n_local = max(0, n_total - n_global)

    global_random = np.random.uniform(0, 1, size=(n_global // 2, d))
    global_lhs = latin_hypercube(n_global - len(global_random), d, seed=42 + func_idx)
    global_candidates = np.vstack([global_random, global_lhs])

    top_k = min(8, len(y))
    top_idx = np.argsort(y)[-top_k:]
    top_points = X[top_idx]

    local_candidates = []
    if n_local > 0:
        per_top = max(1, n_local // top_k)
        for pt in top_points:
            cand = pt + np.random.normal(0, local_noise, size=(per_top, d))
            cand = np.clip(cand, 0.02, 0.98)
            local_candidates.append(cand)

    X_candidates = np.vstack([global_candidates, np.vstack(local_candidates)])

    y_best = np.max(y)
    ei = expected_improvement(X_candidates, gp, y_best, xi=xi)
    best = X_candidates[np.argmax(ei)]

    return best

def main():
    print("\nFinal Round Query Suggestions")
    print("-" * 50)

    lines = []

    for i, func_name in enumerate(FUNCTION_FOLDERS):
        X_old, y_old = load_initial_data(func_name)
        X = np.vstack([X_old] + week_inputs[i])
        y = np.concatenate([y_old, week_outputs[i]])

        query = propose_query(X, y, i)

        print(f"\n{func_name}")
        print(f"Best observed output: {np.max(y):.6f}")
        print(f"Suggested query: {format_query(query)}")

        lines.append(f"{func_name}: {format_query(query)}")

    with open(os.path.join(BASE_DIR, "final_round_queries.txt"), "w", encoding="utf-8") as f:
        for line in lines:
            f.write(line + "\n")

    print("\nSaved to final_round_queries.txt")

if __name__ == "__main__":
    main()